#How to Declare a variable
x = 5

# Print the value of the variable
print(x)  # Output: 5


#How to Declare variables of different data types
x = 5  # Integer
y = 3.14  # Float
z = "hello"  # String
is_admin = True  # Boolean
fruits = ["apple", "banana", "cherry"]  # List
colors = ("red", "green", "blue")  # Tuple
person = {"name": "Scholar", "age": 20}  # Dictionary

# How to Print the values of the variables
print(x)  # Output: 5
print(y)  # Output: 3.14
print(z)  # Output: hello
print(is_admin)  # Output: True
print(fruits)  # Output: ['apple', 'banana', 'cherry']
print(colors)  # Output: ('red', 'green', 'blue')
print(person)  # Output: {'name': 'Scholar', 'age': 20}

# Arithmetic Operations
a = 5
b = 3
print(a + b)  # Output: 8
print(a - b)  # Output: 2
print(a * b)  # Output: 15
print(a / b)  # Output: 1.6666666666666667
print(a % b)  # Output: 2
print(a ** b)  # Output: 125

# Comparison Operations
a = 5
b = 3
print(a == b)  # Output: False
print(a != b)  # Output: True
print(a > b)  # Output: True
print(a < b)  # Output: False
print(a >= b)  # Output: True
print(a <= b)  # Output: False

# Logical Operations
a = True
b = False
print(a and b)  # Output: False
print(a or b)  # Output: True
print(not a)  # Output: False


# If-Else Statement
x = 5
if x > 10:
    print("x is greater than 10")
else:
    print("x is less than or equal to 10")

# For Loop
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# While Loop
i = 0
while i < 5:
    print(i)
    i += 1


